package ExceptionAssignement;

public class Passenger {

    private String name;
    private int age;

    public Passenger(String name, int age) {
        this.name = name;
        this.age = age;
    }
    Passenger()
    {

    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
}
